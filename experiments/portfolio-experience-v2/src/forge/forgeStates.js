export function cameraStateForHash(hash) {
  if (hash === '#work') return 'CAM-03';
  if (hash === '#signals') return 'CAM-02';
  return 'CAM-00';
}

export function cameraStateForBeat(beat) {
  return FORGE_CAMERA_STATES[Math.max(0, Math.min(FORGE_CAMERA_STATES.length - 1, beat))];
}

export function resolveSceneCapability({
  reducedMotion = false,
  saveData = false,
  webgl = true,
  mobileLandscape = false,
  forcedOff = false,
} = {}) {
  if (forcedOff) return { eligible: false, reason: 'forced-off' };
  if (reducedMotion) return { eligible: false, reason: 'reduced-motion' };
  if (saveData) return { eligible: false, reason: 'save-data' };
  if (!webgl) return { eligible: false, reason: 'no-webgl' };
  if (mobileLandscape) return { eligible: false, reason: 'mobile-landscape' };
  return { eligible: true, reason: 'eligible' };
}

export function sceneQualityForViewport(width) {
  if (width <= 767) return 'mobile';
  if (width <= 1100) return 'balanced';
  return 'high';
}
